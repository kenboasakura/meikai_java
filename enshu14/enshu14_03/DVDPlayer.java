package enshu14.enshu14_03;

/*クラス名:DVDPlayer
 *概要:DVDの再生状況を管理
 *作成者:K.Asakura
 *作成日:2024/06/02
 */
public class DVDPlayer {

	/*メソッド名:play
	 *概要:再生開始するメソッド
	 *引数:なし
	 *戻り値:なし
	 *作成者:K.Asakura
	 *作成日:2024/06/02
	 */
	public void play() {
		//再生したことを表示
		System.out.println("■DVD再生開始!");
	}

	/*メソッド名:stop
	 *概要:再生終了するメソッド
	 *引数:なし
	 *戻り値:なし
	 *作成者:K.Asakura
	 *作成日:2024/06/02
	 */
	public void stop() {
		//停止したことを表示
		System.out.println("■DVD再生終了!");
	}

	/*メソッド名:slow
	 *概要:スロー再生開始するメソッド
	 *引数:なし
	 *戻り値:なし
	 *作成者:K.Asakura
	 *作成日:2024/06/02
	 */
	public void slow() {
		//スロー再生したことを表示
		System.out.println("■DVDスロー再生開始!");
	}

}
