package enshu05;

//Scannerクラスの呼び出し
import java.util.Scanner;

/*クラス名:enshu05_03
 *概要:論理値の変数にtrueやfalseを代入して、その値を表示するテキスト
 *作成者:K.Asakura
 *作成日:2024/04/15
 */
public class enshu05_03 {

	/*関数名:main
	 *概要:論理値の変数にtrueやfalseを代入して、その値を表示するテキスト
	 *引数:なし
	 *戻り値:なし
	 *作成者:K.Asakura
	 *作成日:2024/04/15
	 */
	public static void main(String[] args) {
		//Scannerクラスを設定
		Scanner standardInput = new Scanner(System.in);
		
		
		//論理型の変数をtrueを代入して宣言
		boolean firstNumber = true;
		//論理型の変数をfalseを代入して宣言
		boolean secondNumber = false;
		
		//firstNumberはtrueであると表示
		System.out.println("firstNumber="+firstNumber);
		//secondNumberはfalseであると表示
		System.out.println("firstNumber="+secondNumber);
		
		
		
		
		
		
	}

}
